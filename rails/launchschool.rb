# Strings
puts "The man said, 'Hi there!'"
puts 'The man said, \'Hi there!\''

name = "Laurent"
puts "My name is #{name}"

# Symbols
puts :name
puts :a_symbol
puts :"surprisingly, this is also a symbol"

# Numbers
puts 1
puts 1.414

# Operations
# On Numbers
puts 1 + 2
puts 2 * 3
q = 15 / 6
r = 15 % 6
puts "Euclidian division: 15 / 6 : quotient = #{q} and remainder = #{r}"
a = 15.0 / 6
puts "Decimal division: 15 / 6 = #{a}"
# Logical Comparisons
puts 4 == 4
puts 4 == 5
puts 'test' == 'test'
puts 'test' == 'Test'
puts '4' == 4
